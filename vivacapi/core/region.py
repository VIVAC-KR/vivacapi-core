def abbreviate_sido(region_province: str | None) -> str | None:
    """광역시도 풀네임을 약칭으로 변환한다. 매핑에 없으면 원본을 그대로 반환한다."""
    if region_province is None:
        return None
    return SIDO_ABBR.get(region_province, region_province)
